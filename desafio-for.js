// DESAFIO FOR: Imprima o indice e a lista com os seguintes numeros: 100, 200, 300, 400, 500, 600



// Primeira Expressão: É executada apenas uma unica vez.
// Segunda Expressão: Condição de execução.
// Terceira Expressão: É executado sempre ao final do bloco.

const numeros = [100, 200, 300, 400, 500, 600];

for(i = 0; i < numeros.length; i++) {
    console.log(numeros[i])
}